public class Locatario {
    public String nome;
    public Integer idade;
    public Integer dataNascimento;
    public Integer telefone;
    public String sexo;
    
    public Locatario(String nome, Integer idade, Integer dataNascimento, Integer telefone, String sexo) {
        this.nome = nome;
        this.idade = idade;
        this.dataNascimento = dataNascimento;
        this.telefone = telefone;
        this.sexo = sexo;
    }
}
